def glyph_bytes(rows):
    out = []
    for r in range(8):
        line = (rows[r] + "........")[:8] if r < len(rows) else "........"
        b = 0
        for c in range(8):
            if line[c] == "#":
                b |= 1 << (7 - c)
        out.append(b)
    return out


def main():
    first, last = 0x20, 0x5F
    lines = []
    lines.append("/* Generated by port/assets/make_font.py - built-in 8x8 font. */")
    lines.append("#ifndef OTRAIL_FONT8X8_H")
    lines.append("#define OTRAIL_FONT8X8_H")
    lines.append("#include <stdint.h>")
    lines.append(f"#define FONT_FIRST 0x{first:02X}")
    lines.append(f"#define FONT_LAST  0x{last:02X}")
    lines.append("static const uint8_t font8x8[FONT_LAST - FONT_FIRST + 1][8] = {")
    for code in range(first, last + 1):
        ch = chr(code)
        rows = G.get(ch, G[" "])
        b = glyph_bytes(rows)
        body = ", ".join(f"0x{v:02x}" for v in b)
        lines.append(f"    {{ {body} }},  /* 0x{code:02X} '{ch}' */")
    lines.append("};")
    lines.append("#endif")
    open("port/core/font8x8.h", "w", encoding="utf-8").write("\n".join(lines) + "\n")
    print(f"wrote port/core/font8x8.h ({last - first + 1} glyphs)")


if __name__ == "__main__":
    main()
