int unit_0006_entrypoint_finalize_terminal_handoff(
    uint16_t bx_seed,
    const Unit0006TerminalHeader *header,
    Unit0006TerminalHandoff *out
) {
    if (header == 0 || out == 0) {
        return 0;
    }

    out->final_ds = (uint16_t)(bx_seed - 0x0010u);
    out->final_es = out->final_ds;
    out->final_ss = (uint16_t)(header->stack_seg_rel + bx_seed);
    out->final_sp = header->stack_off;
    out->jump_off = header->jump_off;
    out->jump_seg = (uint16_t)(header->jump_seg_word + bx_seed);
    out->cli_seen = 1u;
    out->sti_seen = 1u;
    out->bx_cleared = 1u;
    return 1;
}

int unit_0006_entrypoint_describe_mixed_slice(Unit0006MixedSlice *out) {
    if (out == 0) {
        return 0;
    }

    out->begins_mid_instruction = 1u;
    out->leading_xor_tail_byte = 0xDBu;
    out->handoff_code_size = UNIT_0006_HANDOFF_CODE_SIZE;
    out->postjump_data_size = UNIT_0006_POSTJUMP_DATA_SIZE;
    return 1;
}
